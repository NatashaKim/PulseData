class Sketch {
  slWidth
  slHeight
  slCount
  xPosStart
  yPosStart
  slices
  imageName
  imageName1
  fileName
  index
  index1

  constructor(imageName, imageName1, frameName, window) {
    this.slWidth = 10
    this.slHeight = 563
    this.slCount = 100
    this.xPosStart = 0 // wrt canvas
    this.yPosStart = 0 // wrt canvas
    this.slices = [] // array for storing slice objects
    this.imageName = imageName
    this.imageName1 = imageName1
    this.frameName = frameName
    this.index = 0
    index1 = 0

    window.setInterval(() => {
      this.nextIndex()
    }, 20)
  }

  origImg
  origImg1

  nextIndex() {
    this.index = this.index + 1
    if (this.index >= this.slices.length) {
      this.index = 0
    }

    index1 = index1 + 1
    if (index1 >= pulseData.length) {
      index1 = 0
    }
  }

  sketch(p) {
    p.preload = () => {
      this.origImg = p.loadImage(this.imageName)
      this.origImg1 = p.loadImage(this.imageName1)
    }
    p.setup = () => {
      const canvas = p.createCanvas(1440, 800)
      canvas.parent(this.frameName)

      let gBgColor = p.color(252, 252, 252)
      p.background(gBgColor)

      for (let i = 0; i < this.slCount; i++) {
        this.slices[i] = new SliceObj(
          i,
          this.xPosStart,
          this.yPosStart,
          this.slWidth,
          this.slHeight,
          this.origImg,
          this.origImg1,
          p
        )
      }
    }

    p.draw = () => {
      p.background('#FCFCFC')
      for (let i = 0; i < this.slCount; i++) {
        this.slices[i].display(i)
        this.slices[i].update(i, this.index, p.mouseY) //?
        //  slices[i].display(i);
      }
    }
  }
}

